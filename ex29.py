people = 20
cats = 30
dogs = 15

if people < cats:
    print("Too many cats")

if people > cats:
    print("Not too many cats")

if people < dogs:
    print("Dogs win")

if people > dogs:
    print("Dogs lose")

dogs += 5

if people >= dogs:
    print("People more or equal then dogs")

if people <= dogs:
    print("People less or equal then dogs")

if people == dogs:
    print("People are equal to dogs")
